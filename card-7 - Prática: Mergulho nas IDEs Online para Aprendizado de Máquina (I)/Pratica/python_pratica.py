# Operações Básicas em python

# Soma
soma = 5 + 3
print('soma:', soma)

# Subtrair
sub = 10 - 4
print('subritação:', sub)

# Mutiplicar
mult = 6 * 7
print('mutiplicação', mult)

# Dividir
div = 20 / 5
print('divisão', div)

# Potência
pot = 2 ** 3
print('Potêcia:', pot)

# Modulo
resto = 17 % 3
print('Resto de divisão:', resto)
